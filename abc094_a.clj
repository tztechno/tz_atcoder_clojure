(def a (read))
(def b (read))
(def x (read))
(def ans (<= a x (+ a b)))
(print (if ans "YES" "NO"))
####################################
(defn compare-strings []
  (let [[a b x] (map #(Integer/parseInt %) (clojure.string/split (read-line) #" "))]
    (if (and (<= a x)
             (<= x (+ a b)))
      (println "YES")
      (println "NO"))))
(compare-strings)
