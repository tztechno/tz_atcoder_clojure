(defn even [e]
  (* e e))

(defn odd [o]
  (* o (+ o 1)))

(defn pair [k]
  (if (even? k)
    (even (quot k 2))
    (odd (quot k 2))))


(let [input (read-line)
      k (Integer/parseInt input)]
  (println (pair k)))
