(defn abcofABC [S](if (= (sort S) '(\a \b \c)) "Yes" "No"))
(println (abcofABC (read-line)))
####################################
(let [s (read-line)]
  (if (and (not= (get s 0) (get s 1))
           (not= (get s 1) (get s 2))
           (not= (get s 2) (get s 0)))
    (println "Yes")
    (println "No")))
